#include "application.hpp"


gui::Application::Application(const char* title, int width, int height)
    :m_title(title),
    m_width(width),
    m_height(height)
{
    initialize();
}


gui::Application::~Application()
{
    // Cleanup
    ImGui_ImplOpenGL3_Shutdown();
    ImGui_ImplGlfw_Shutdown();
    ImGui::DestroyContext();

    glfwDestroyWindow(m_window);
    glfwTerminate();
}


void gui::Application::initialize()
{
    if(!init_glfw())
        printf("Error: Initialization failed\n");
    config_imgui();
}

bool gui::Application::init_glfw()
{
    glfwSetErrorCallback(glfw_error_callback);

    if(!glfwInit())
    {
        printf("failed to initialize glfw\n");
        return 0;
    }

    // Decide GL+GLSL versions
#if defined(IMGUI_IMPL_OPENGL_ES2)
    // GL ES 2.0 + GLSL 100
     m_glsl_version = "#version 100";
    glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 2);
    glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 0);
    glfwWindowHint(GLFW_CLIENT_API, GLFW_OPENGL_ES_API);
#elif defined(__APPLE__)
    // GL 3.2 + GLSL 150
    m_glsl_version = "#version 150";
    glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 3);
    glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 2);
    glfwWindowHint(GLFW_OPENGL_PROFILE, GLFW_OPENGL_CORE_PROFILE);  // 3.2+ only
    glfwWindowHint(GLFW_OPENGL_FORWARD_COMPAT, GL_TRUE);            // Required on Mac
#else
    // GL 3.0 + GLSL 130
    m_glsl_version = "#version 130";
    glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 3);
    glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 0);
    //glfwWindowHint(GLFW_OPENGL_PROFILE, GLFW_OPENGL_CORE_PROFILE);  // 3.2+ only
    //glfwWindowHint(GLFW_OPENGL_FORWARD_COMPAT, GL_TRUE);            // 3.0+ only
#endif

    m_window = glfwCreateWindow(m_width, m_height, m_title.c_str(), nullptr, nullptr);
    if(m_window == nullptr)
    {        
        printf("failed to create glfw window\n");
        return 0;
    }


    glfwMakeContextCurrent(m_window);
    glfwSwapInterval(1); // Enable vsync

    if(!gladLoadGLLoader((GLADloadproc) glfwGetProcAddress))
    {
        printf("failed to load glad\n");
        return 0;
    }

    //callbacks
    glfwSetMouseButtonCallback(m_window, &mouse_button_callback);
    glfwSetCursorPosCallback(m_window, &cursor_pos_callback);
    glfwSetKeyCallback(m_window, &key_callback);

    return 1;
}



